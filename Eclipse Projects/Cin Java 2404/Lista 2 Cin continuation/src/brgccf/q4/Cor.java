package brgccf.q4;

public class Cor {
	private int R;
	private int G;
	private int B;
	private String nomecor;
	public Cor (int r, int g, int b, String nomecor){
		R = r;
		G = g;
		B = b;
		this.nomecor = nomecor;
	}
	
	public int getR(){
		return R;
	}
	public int getG(){
		return G;
	}
	public int getB(){
		return B;
	}
	public String getNomeCor(){
		return this.nomecor;
	}
	
	
	
	
	
	
	
	

}
